#!/usr/bin/python3
"""defines a class for rectangles."""
from models.base import Base


class Rectangle(Base):
    """Display a rectangle."""

    def __init__(self, width, height, x=0, y=0, id=None):
        """Init the new Rectangle.

        Arguments as following:
            width (int): The new Rectangle's width.
            height (int): The new Rectangle's height.
            x (int): The new Rectangle's x coordinate.
            y (int): The new Rectangle y coordinate.
            id (int): The new Rectangle identity.
        Raises as below:
            TypeError: if neither width nor height are integers.
            ValueError: If either width or height is zero, then.
            TypeError: if neither x nor y is an integer.
            ValueError: If either x or y is zero, then.
        """
        self.width = width
        self.height = height
        self.x = x
        self.y = y
        super().__init__(id)

    @property
    def width(self):
        """Set/get the Rectangle's width."""
        return self.__width

    @width.setter
    def width(self, value):
        if type(value) != int:
            raise TypeError("width must be an integer")
        if value <= 0:
            raise ValueError("width must be > 0")
        self.__width = value

    @property
    def height(self):
        """Set/get the Rectangle's height."""
        return self.__height

    @height.setter
    def height(self, value):
        if type(value) != int:
            raise TypeError("height must be an integer")
        if value <= 0:
            raise ValueError("height must be > 0")
        self.__height = value

    @property
    def x(self):
        """Set/get the Rectangle's x coordinate."""
        return self.__x

    @x.setter
    def x(self, value):
        if type(value) != int:
            raise TypeError("x must be an integer")
        if value < 0:
            raise ValueError("x must be >= 0")
        self.__x = value

    @property
    def y(self):
        """Set/get the Rectangle's y coordinate."""
        return self.__y

    @y.setter
    def y(self, value):
        if type(value) != int:
            raise TypeError("y must be an integer")
        if value < 0:
            raise ValueError("y must be >= 0")
        self.__y = value

    def area(self):
        """Return the Rectangle's area."""
        return self.width * self.height

    def display(self):
        """Use the '#' character to print the rectangle."""
        if self.width == 0 or self.height == 0:
            print("")
            return

        [print("") for y in range(self.y)]
        for h in range(self.height):
            [print(" ", end="") for x in range(self.x)]
            [print("#", end="") for w in range(self.width)]
            print("")

    def update(self, *args, **kwargs):
        """Rectangle should be updated.

        Arguments as following:
            *args (ints): updated attribute values.
                - The first argument is the id attribute.
                - the second argument is the width attribute
                - The 3rd argument is the height attribute
                - the fourth argument is the x attribute
                - The fifth argument is y attribute
            **kwargs (dict): New key/value pairs of attributes.
        """
        if args and len(args) != 0:
            a = 0
            for arg in args:
                if a == 0:
                    if arg is None:
                        self.__init__(self.width, self.height, self.x, self.y)
                    else:
                        self.id = arg
                elif a == 1:
                    self.width = arg
                elif a == 2:
                    self.height = arg
                elif a == 3:
                    self.x = arg
                elif a == 4:
                    self.y = arg
                a += 1

        elif kwargs and len(kwargs) != 0:
            for k, v in kwargs.items():
                if k == "id":
                    if v is None:
                        self.__init__(self.width, self.height, self.x, self.y)
                    else:
                        self.id = v
                elif k == "width":
                    self.width = v
                elif k == "height":
                    self.height = v
                elif k == "x":
                    self.x = v
                elif k == "y":
                    self.y = v

    def to_dictionary(self):
        """Return a Rectangle's dictionary representation."""
        return {
            "id": self.id,
            "width": self.width,
            "height": self.height,
            "x": self.x,
            "y": self.y
        }

    def __str__(self):
        """Return the Rectangle's print() and str() representations."""
        return "[Rectangle] ({}) {}/{} - {}/{}".format(self.id,
                                                       self.x, self.y,
                                                       self.width, self.height)
